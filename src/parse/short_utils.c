#include "parse.h"

int	ft_strlenspace(char *str)
{
	int	i;

	i = 0;
	while (str[i] && str[i] != ' ')
	{
		i++;
		if (str[i] == '\"')
			skip_quote(str, &i, '\"', '\\');
		if (str[i] == '\'')
			skip_quote(str, &i, '\'', FALSE);
	}
	return (i);
}
